package com.example.bankcards.entity.enums;

/**
 * Перечисление статусов банковской карты.
 * Определяет текущее состояние карты и доступные операции
 */
public enum CardStatus {

    /**
     * Активная карта - доступны все операции (пополнение, перевод, оплата)
     * Карта может использоваться для проведения транзакций
     */
    ACTIVE,

    /**
     * Заблокированная карта - все операции запрещены
     * Карта временно недоступна для использования.
     * Может быть разблокирована администратором или по запросу владельца
     */
    BLOCKED,

    /**
     * Просроченная карта - карта больше не действительна
     * Срок действия карты истек, операции невозможны.
     * Требуется выпуск новой карты
     */
    EXPIRED
}
